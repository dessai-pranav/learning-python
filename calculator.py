def add(n1, n2):
    return n1 + n2

def subtract(n1, n2):
    return n1 - n2

def multiply(n1, n2):
    return n1 * n2

def divide(n1, n2):
    return n1 / n2

operations = {
    "+" : add,
    "-" : subtract,
    "*" : multiply,
    "/" : divide,
}

def calculator():
    n1 = float(input("Enter first number: "))
    accumulate =  True

    while accumulate :
        for key in operations:
            print(key)

        oper = input("Enter an operation: ")
        n2 = float(input("Enter second number: "))

        if oper in operations:
            result = operations[oper](n1, n2)
            print(f"{n1} {oper} {n2} = {result}")
        else:
            print("Invalid operation")
            continue

        wanna_continue = input(
            f"Type 'y' to continue with {result}, or 'n' to start new: "
        )

        if wanna_continue == "y":
            n1 = result
        else:
            accumulate = False
            print("\n " * 20)
            calculator()


calculator()
